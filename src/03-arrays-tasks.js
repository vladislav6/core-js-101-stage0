function getIdentityMatrix(n) {
  const result = Array(n)
    .fill(1)
    .map(() => Array(n).fill(0));
  const i = 0;
  function count(a, c) {
    const arr = [...a];
    if (c < arr.length) {
      arr[c][c] = 1;
      const k = c + 1;
      count(arr, k);
    }
  }
  count(result, i);
  return result;
}

